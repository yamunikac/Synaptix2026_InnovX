import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are VisionPro AI Assistant â€” a helpful image processing expert. You help users with:
- Image editing tasks (filters, cropping, compression, format conversion, rotation, background removal)
- Choosing the right tool for their needs
- Understanding image formats and quality settings
- Tips for best results with each tool

Available tools on this website:
1. Image Filters Studio (/studio) - 13 filters including grayscale, blur, edge detection, cartoon, brightness/contrast, sharpen, emboss, pixelate, sepia, invert
2. Enhance & Clarity (/enhance) - Sharpen, denoise, auto-contrast, low-light fix
3. Compress & Resize (/compress) - Reduce file size with quality control
4. Convert Format (/convert) - Convert between PNG, JPEG, WebP, BMP
5. Crop & Adjust (/crop) - Crop with aspect ratios, drag handles on edges
6. Rotate & Straighten (/rotate) - Any angle rotation, flip
7. Background Removal (/remove-bg) - Auto and manual paint modes

Keep answers concise and helpful. If user asks to perform a task, guide them to the right tool.`,
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
